static const uint8_t rcon[11] = {
    0x00, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36
};

static uint32_t subword(uint32_t w)
{
    return ((uint32_t)sbox[(w >> 24) & 0xff] << 24) |
           ((uint32_t)sbox[(w >> 16) & 0xff] << 16) |
           ((uint32_t)sbox[(w >>  8) & 0xff] <<  8) |
           ((uint32_t)sbox[ w        & 0xff]);
}

static uint32_t rotword(uint32_t w)
{
    return (w << 8) | (w >> 24);
}

void aes256_init(aes256_ctx *ctx, const uint8_t key[AES_KEY_SIZE])
{
    /* Load key into first 8 round-key words */
    for (int i = 0; i < 8; i++)
        ctx->rk[i] = ((uint32_t)key[4*i] << 24) |
                      ((uint32_t)key[4*i+1] << 16) |
                      ((uint32_t)key[4*i+2] << 8) |
                      ((uint32_t)key[4*i+3]);

    /* Expand to 60 words */
    for (int i = 8; i < 60; i++) {
        uint32_t tmp = ctx->rk[i - 1];
        if (i % 8 == 0)
            tmp = subword(rotword(tmp)) ^ ((uint32_t)rcon[i / 8] << 24);
        else if (i % 8 == 4)
            tmp = subword(tmp);
        ctx->rk[i] = ctx->rk[i - 8] ^ tmp;
    }
}

/* GF(2^8) multiply for MixColumns */
static uint8_t xtime(uint8_t x)
{
    return (uint8_t)((x << 1) ^ (((x >> 7) & 1) * 0x1b));
}

static uint8_t gmul(uint8_t a, uint8_t b)
{
    uint8_t p = 0;
    for (int i = 0; i < 8; i++) {
        if (b & 1) p ^= a;
        uint8_t hi = a & 0x80;
        a <<= 1;
        if (hi) a ^= 0x1b;
        b >>= 1;
    }
    return p;
}

void aes256_encrypt_block(const aes256_ctx *ctx, const uint8_t in[16], uint8_t out[16])
{
    uint8_t s[16];
    memcpy(s, in, 16);
    int Nr = 14; /* AES-256 rounds */

    /* AddRoundKey(0) */
    for (int i = 0; i < 16; i++)
        s[i] ^= (uint8_t)(ctx->rk[i / 4] >> (24 - 8 * (i % 4)));

    for (int round = 1; round <= Nr; round++) {
        /* SubBytes */
        for (int i = 0; i < 16; i++)
            s[i] = sbox[s[i]];

        /* ShiftRows */
        uint8_t t;
        t = s[1]; s[1] = s[5]; s[5] = s[9]; s[9] = s[13]; s[13] = t;
        t = s[2]; s[2] = s[10]; s[10] = t; t = s[6]; s[6] = s[14]; s[14] = t;
        t = s[15]; s[15] = s[11]; s[11] = s[7]; s[7] = s[3]; s[3] = t;

        /* MixColumns (skip on last round) */
        if (round < Nr) {
            for (int c = 0; c < 4; c++) {
                int base = c * 4;
                uint8_t a0 = s[base], a1 = s[base+1], a2 = s[base+2], a3 = s[base+3];
                s[base]   = xtime(a0) ^ xtime(a1) ^ a1 ^ a2 ^ a3;
                s[base+1] = a0 ^ xtime(a1) ^ xtime(a2) ^ a2 ^ a3;
                s[base+2] = a0 ^ a1 ^ xtime(a2) ^ xtime(a3) ^ a3;
                s[base+3] = xtime(a0) ^ a0 ^ a1 ^ a2 ^ xtime(a3);
            }
        }

        /* AddRoundKey */
        for (int i = 0; i < 16; i++)
            s[i] ^= (uint8_t)(ctx->rk[round * 4 + i / 4] >> (24 - 8 * (i % 4)));
    }

    memcpy(out, s, 16);
}

void aes256_decrypt_block(const aes256_ctx *ctx, const uint8_t in[16], uint8_t out[16])
{
    uint8_t s[16];
    memcpy(s, in, 16);
    int Nr = 14;

    /* AddRoundKey(Nr) */
    for (int i = 0; i < 16; i++)
        s[i] ^= (uint8_t)(ctx->rk[Nr * 4 + i / 4] >> (24 - 8 * (i % 4)));

    for (int round = Nr - 1; round >= 0; round--) {
        /* InvShiftRows */
        uint8_t t;
        t = s[13]; s[13] = s[9]; s[9] = s[5]; s[5] = s[1]; s[1] = t;
        t = s[2]; s[2] = s[10]; s[10] = t; t = s[6]; s[6] = s[14]; s[14] = t;
        t = s[3]; s[3] = s[7]; s[7] = s[11]; s[11] = s[15]; s[15] = t;

        /* InvSubBytes */
        for (int i = 0; i < 16; i++)
            s[i] = rsbox[s[i]];

        /* AddRoundKey */
        for (int i = 0; i < 16; i++)
            s[i] ^= (uint8_t)(ctx->rk[round * 4 + i / 4] >> (24 - 8 * (i % 4)));

        /* InvMixColumns (skip on round 0) */
        if (round > 0) {
            for (int c = 0; c < 4; c++) {
                int base = c * 4;
                uint8_t a0 = s[base], a1 = s[base+1], a2 = s[base+2], a3 = s[base+3];
                s[base]   = gmul(a0,0x0e) ^ gmul(a1,0x0b) ^ gmul(a2,0x0d) ^ gmul(a3,0x09);
                s[base+1] = gmul(a0,0x09) ^ gmul(a1,0x0e) ^ gmul(a2,0x0b) ^ gmul(a3,0x0d);
                s[base+2] = gmul(a0,0x0d) ^ gmul(a1,0x09) ^ gmul(a2,0x0e) ^ gmul(a3,0x0b);
                s[base+3] = gmul(a0,0x0b) ^ gmul(a1,0x0d) ^ gmul(a2,0x09) ^ gmul(a3,0x0e);
            }
        }
    }

    memcpy(out, s, 16);
}

/* ---- CBC mode with PKCS7 padding ---- */

static void read_urandom(uint8_t *buf, size_t len)
{
    FILE *f = fopen("/dev/urandom", "rb");
    if (!f) { perror("urandom"); exit(1); }
    if (fread(buf, 1, len, f) != len) { perror("fread urandom"); exit(1); }
    fclose(f);
}

uint8_t *aes_cbc_encrypt(const uint8_t key[AES_KEY_SIZE],
                          const uint8_t *plain, size_t plain_len,
                          size_t *out_len)
{
    aes256_ctx ctx;
    aes256_init(&ctx, key);

    /* PKCS7 padding */
    size_t pad = AES_BLOCK_SIZE - (plain_len % AES_BLOCK_SIZE);
    size_t padded_len = plain_len + pad;

    uint8_t *padded = malloc(padded_len);
    if (!padded) return NULL;
    memcpy(padded, plain, plain_len);
    memset(padded + plain_len, (uint8_t)pad, pad);

    /* Output = IV (16) + ciphertext (padded_len) */
    *out_len = AES_IV_SIZE + padded_len;
    uint8_t *result = malloc(*out_len);
    if (!result) { free(padded); return NULL; }

    /* Generate random IV */
    uint8_t *iv = result;
    read_urandom(iv, AES_IV_SIZE);

    /* CBC encrypt */
    uint8_t prev[AES_BLOCK_SIZE];
    memcpy(prev, iv, AES_BLOCK_SIZE);

    for (size_t i = 0; i < padded_len; i += AES_BLOCK_SIZE) {
        uint8_t block[AES_BLOCK_SIZE];
        for (int j = 0; j < AES_BLOCK_SIZE; j++)
            block[j] = padded[i + j] ^ prev[j];
        aes256_encrypt_block(&ctx, block, result + AES_IV_SIZE + i);
        memcpy(prev, result + AES_IV_SIZE + i, AES_BLOCK_SIZE);
    }

    free(padded);
    return result;
}

uint8_t *aes_cbc_decrypt(const uint8_t key[AES_KEY_SIZE],
                          const uint8_t *data, size_t data_len,
                          size_t *out_len)
{
    if (data_len < AES_IV_SIZE + AES_BLOCK_SIZE) return NULL;

    size_t ct_len = data_len - AES_IV_SIZE;
    if (ct_len % AES_BLOCK_SIZE != 0) return NULL;

    aes256_ctx ctx;
    aes256_init(&ctx, key);

    const uint8_t *iv = data;
    const uint8_t *ct = data + AES_IV_SIZE;

    uint8_t *plain = malloc(ct_len);
    if (!plain) return NULL;

    uint8_t prev[AES_BLOCK_SIZE];
    memcpy(prev, iv, AES_BLOCK_SIZE);

    for (size_t i = 0; i < ct_len; i += AES_BLOCK_SIZE) {
        uint8_t block[AES_BLOCK_SIZE];
        aes256_decrypt_block(&ctx, ct + i, block);
        for (int j = 0; j < AES_BLOCK_SIZE; j++)
            plain[i + j] = block[j] ^ prev[j];
        memcpy(prev, ct + i, AES_BLOCK_SIZE);
    }

    /* Validate and remove PKCS7 padding */
    uint8_t pad = plain[ct_len - 1];
    if (pad == 0 || pad > AES_BLOCK_SIZE) { free(plain); return NULL; }
    for (size_t i = ct_len - pad; i < ct_len; i++) {
        if (plain[i] != pad) { free(plain); return NULL; }
    }

    *out_len = ct_len - pad;
    return plain;
}
